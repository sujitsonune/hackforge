  }

  static List<Map<String, dynamic>> getPlatformsByCategory(String category) {
    return getAllPlatforms()
        .where((platform) => platform['category'] == category)
        .toList();
  }

  static Map<String, dynamic>? getPlatformByName(String name) {
    try {
      return getAllPlatforms().firstWhere(
        (platform) => platform['name'].toLowerCase() == name.toLowerCase(),
      );
    } catch (e) {
      return null;
    }
  }
